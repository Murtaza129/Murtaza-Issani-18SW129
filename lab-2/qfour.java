class qfour{
    public static void main (String args[]){
        
        float pi = 3.14f,rad = 16f;
        float circum = 2f * pi * rad;
        float diameter = 2f * rad;
        float area = pi * (rad * rad);
        System.out.println("pi="+ pi+ "\nradius" + rad);
        System.out.println("\ncircumference="+circum+"\ndiameter"+diameter);
        System.out.println("\narea="+area);
}
}
